#シンボルでは、文字列の前にコロン(:)を定義します。
#文字列では、シングルクォーテーション（''）やダブルクォーテーション（""）で囲んで定義します。

 # シンボル    :プログラミング学習
 # 文字列     "プログラミング学習"


# シンボルは変更することができませんが、文字列は変更可能なオブジェクトです。


#シンボルとは
#見た目は文字列のようで、内部では整数として扱われているオブジェクト
#文字列の前に、コロン(:)を付ける


#シンボルと文字列の違いとは
#シンボルは文字列の前にコロン(:)で定義する
#　文字列はダブルクォーテーション（""）で囲んで定義する
#シンボルは書き換えられない
#　文字列は変更可能
#シンボルは同じオブジェクトを参照している(一意性がある)
#　文字列は同じオブジェクトではない
#シンボルは文字列よりも処理が高速


